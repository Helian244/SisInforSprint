    private void inActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inActionPerformed
        String nombrePark = nomPark.getText();
        String valorPark = valPark.getText();
        int valorInt = Integer.valueOf(valorPark);
        if(nombrePark.equals("") || valorPark.equals("")){
            JOptionPane.showMessageDialog(null, "Se detectatron campos vacios, debe llenar par completar la acción");
        }else{
            if(this.existeParque(nombrePark,valorPark) == 0){
                try{
            CallableStatement c = conectar.prepareCall("{call añadirParque(?,?)}");
            c.setString(1,nombrePark);
            c.setInt(2,valorInt);
            c.execute();
            c.close();
            JOptionPane.showMessageDialog(null, "Se añadio con exito el parque");
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "error: " + e.toString());
        }
            }else{
                JOptionPane.showMessageDialog(null, "El parque que desea ingresar ya existe");
            }
        }
    }//GEN-LAST:event_inActionPerformed

