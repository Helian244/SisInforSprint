    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      Conexion cc=new Conexion();
      Connection cn = cc.establecerConexion();
      
      String selectedValue = (String) jComboBox1.getSelectedItem();
      try{
          PreparedStatement pst=cn.prepareStatement("INSERT INTO Materia(Carrera,Semestre,NombreMateria) VALUES(?,?,?)");
          
          pst.setString(1,jTextField1.getText());
          pst.setString(2,selectedValue);
          pst.setString(3,jTextField3.getText());
          
          
          int a =pst.executeUpdate();
          if(a>0){
            JOptionPane.showMessageDialog(null,"Registro exitoso");
            mostrardatos("");
          }
          else{
            JOptionPane.showMessageDialog(null, "error al registrar");
            
          }

      } catch (SQLException ex) {
            //ex.printStackTrace();

        }
    }//GEN-LAST:event_jButton1ActionPerformed

