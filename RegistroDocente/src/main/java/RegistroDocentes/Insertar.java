    void CargarTabla(String valor) {
        String sSQL = "";
        modelo = new DefaultTableModel(null, titulos);
        Conexion mysql = new Conexion();
        Connection cn = mysql.establecerConexion();
        sSQL = "SELECT * FROM Docente WHERE id LIKE '%" + valor + "%'";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sSQL);
            while (rs.next()) {
                datos[0] = rs.getString("id");
                datos[1] = rs.getString("nombre");
                datos[2] = rs.getString("apellidopaterno");
                datos[3] = rs.getString("apellidomaterno");
                datos[4] = rs.getString("edad");
                datos[5] = rs.getString("genero");
                datos[6] = rs.getString("direccion");
                modelo.addRow(datos);
            }
            tbldoc.setModel(modelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex, sSQL, WIDTH);
        }

    }

